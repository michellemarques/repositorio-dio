function mostraIdade(nome, idade) {
    return `A idade de ${nome} é ${idade}.` 
};

function mostraICidade(nome, idade) {
    return `A cidade de ${nome} é ${idade}.` 
};

function mostraHobby(nome, idade) {
    return `O hobby de ${nome} é ${idade}.` 
};

export {
    mostraIdade,
    mostraICidade,
    mostraHobby
};
